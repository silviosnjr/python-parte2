frase = "  python é uma poderosa Linguagem de Programação!"

#Para deixar tudo minusculo .lower()
print(frase.lower())

#Para deixar tudo maiúsculo .upper()
print(frase.upper())

#Para deixar a primeira letra maiscula .capitalize()
print(frase.strip().capitalize())

#Para remover os espaços .strip()
print(frase.strip())